package bean;

public class beanData {
	
	private String Code, Name;
	
	public beanData () {
		Code = new String();
		Name = new String();
	}

	public String getCode() {
		return Code;
	}

	public void setCode(String strCode) {
		this.Code = strCode;
	}

	public String getName() {
		return Name;
	}

	public void setName(String strName) {
		this.Name = strName;
	}
	
	
	 
}
